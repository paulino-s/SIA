    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila = tabla.getSelectedRow();
       
        if (fila >= 0) {
            txtcodigo.setText(tabla.getValueAt(fila, 0).toString());   
            txtnombre.setText(tabla.getValueAt(fila, 1).toString());
            combocat.setSelectedItem(tabla.getValueAt(fila, 2).toString()); 
            txtcantidad.setText(tabla.getValueAt(fila, 3).toString());
            txtprecio.setText(tabla.getValueAt(fila, 4).toString());
            txtprecioventa.setText(tabla.getValueAt(fila, 5).toString());
        noeditable();
        btnactualizar.setEnabled(false);
        btneliminar.setEnabled(true);
        btneditar.setEnabled(true);
        btnguardar.setEnabled(false);
            
            
        }else{
        
        
        }        
    
    }//GEN-LAST:event_tablaMouseClicked

