    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        noeditable();
        codigo = txtcodigo.getText();
        nombre = txtnombre.getText();
        categoria = combocat.getSelectedItem().toString();
        cantidad = Integer.parseInt(txtcantidad.getText());
        precio = Double.parseDouble(txtprecio.getText());
        precio_ven = Double.parseDouble(txtprecioventa.getText());
        
        btnactualizar.setEnabled(false);
        try {
        
        produc.actualizar(codigo,nombre, categoria, cantidad, precio, precio_ven);    
        tabla.setModel(produc.cargar(""));    
// TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(agregar_p.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

